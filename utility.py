import json
import webbrowser


# ----------------------------------------------------------------------------------------------------
# jsonファイルの読み込み
# ----------------------------------------------------------------------------------------------------
def ReadFileJson(filepath):
		
	with open(filepath) as f:
		read_json = json.load(f)
	return read_json

# ----------------------------------------------------------------------------------------------------
# launchを使用してHome画面に戻る
# ----------------------------------------------------------------------------------------------------
def ReturnHomeScreen():
	webbrowser.open('launcher://homescreen')


# ----------------------------------------------------------------------------------------------------
# launchを使用してショートカット画面に戻る
# ----------------------------------------------------------------------------------------------------
def ReturnShortcutScreen():
	webbrowser.open('shortcuts://')


# ----------------------------------------------------------------------------------------------------
# URLを開く
# ----------------------------------------------------------------------------------------------------
def OpenWebBrowser(url):
	webbrowser.open(url)
